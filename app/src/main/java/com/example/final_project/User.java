package com.example.final_project;

 public class User {

    public String firstName, age, email, phNumber, lastName ;

    public User(){

    }

    public User(String firstName, String age, String email, String phNumber, String lastName) {
        this.firstName = firstName;
        this.age = age;
        this.email = email;
        this.phNumber = phNumber;
        this.lastName = lastName;
    }
}